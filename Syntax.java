// void is only written type. it has no value
// function name main
// public static - access modifier
// void is return type and has no value.
// code written under class is called methods.
// main is function.
public class Syntax {
    public static void printHelloWorld(){
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
    }
    public static void main (String args[]){
        printHelloWorld();//function call
    }
}
